class Variable01 {
    public static void main(String args[]){
        float tambah,bagi,kurang,kali, a, b;

        a=20;
        b=50;

        tambah=(a+b);
        kali=(a*b);
        kurang=(b-a);
        bagi=(b/a);

        System.out.println(a+"+"+b+" = "+tambah);
        System.out.println(a+"*"+b+" = "+kali);
        System.out.println(b+"-"+a+" = "+kurang);
        System.out.println(b+"/"+a+" = "+bagi);
    }
}